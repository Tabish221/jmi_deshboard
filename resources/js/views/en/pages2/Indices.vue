Futures Markets are markets in which participants can fix the price they will  pay or receive for bonds, shares and currencies and other financial products,  in the future (effectively the parties thus &quot;lock into&quot; a known  exchange rate/price). <br />
<br />
Trading is made by buying or selling futures contracts which are standardized  according to the quality, quantity, delivery time and location for each  instrument. A futures contract is specified with the month during which the  delivery or settlement is to occur i.e. if the product is gold and delivery is  in July then the price quoted is for July Gold.<br />
There are three types of participants in futures markets, the one that wants  physical delivery, the hedger who wishes to protect himself/herself against  adverse movements in prices and the speculative investor. <br />
The speculative investor has no intention of making or taking delivery of the  commodity but, rather, seeks to profit from a change in the price. Investors  buy a product when they anticipate rising prices i.e. entering long (and sell  that product later, at the higher price), or sell a product when they  anticipate declining prices i.e. entering short (and then buy that product  later, at the lower price). <br />
<br />
If you speculate in futures contracts and the price moves in the direction you  anticipated, then you will be making profit. Conversely, if prices move in the  opposite direction then losses are made. Speculators therefore are individuals  and corporations who seek to profit from anticipated increases or decreases in  futures prices.<br />
