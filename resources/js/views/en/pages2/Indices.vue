The principle of buying and selling for future delivery has characterized  the markets for over a century and a half in physical commodities, mainly  metals and staple foodstuffs. It has also been the feature of the foreign  exchange markets, where prices can be agreed today for foreign currencies and  other financial instruments that can be delivered in the future.<br />
