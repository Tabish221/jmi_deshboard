            <h2 id="stops">Trailing Stops</h2>
            <p>
                - Set a stop loss first<br>
                A common mistake among traders is to rely upon a trailing stop as if it were a stop loss. It is important to remember that a trailing stop is not triggered until a trade is positive the amount of pips the trailing stop was set for (+ 1). In other words, a trailing stop set for 15 pips does not become your stop loss until the market has gone at least 16 pips in your favor.<br>
                If the market were to immediately trend against your position you would have no protection in the form of a stop loss.
                <br>
                - How does a trailing stop work?<br>
                Trailing stops are designed to lock in profit levels. Trailing stops literally trail along your increasing profit and adjust your stop loss levels accordingly. A trailing stop set for 15 pips will trigger once a trader's position is positive at least 16 pips. From this level the trailing stop will adjust pip for pip as your profit level continues to increase. A trader whose trailing stop is set to 15 pips and who is positive 20 pips has protected at least 5 pips of profit. As this trader's position increases so will their protected profit level.<br>
                <br>
                - Does my computer need to be on for the trailing stop to work?<br>
                Once a trailing stop is triggered a new stop loss is sent from your computer to JMI Brokers servers. However, for your trailing stop to continue to send updated stop losses to our servers, your computer does need to be up and running. Essentially, if your trailing stop has been triggered you do have a stop loss safely in place. However, if your computer is shut down your stop loss will not continue to trail
            </p>



