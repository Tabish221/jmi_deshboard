      <p>Before closing this chapter it is  necessary to delineate the basic rules of the overall Elliot Wave structure.  They are as follows:</p>
      <ol start="1" type="1">
        <li>A cycle may not be said to be complete until a 5-3       pattern is traced out.</li>
      </ol>
      <ol start="2" type="1">
        <li>Waves are measured by distance covered on the chart (in       terms of share price), not by time. (Time is relatively unimportant in our       study of Elliot Wave Theory)</li>
      </ol>
      <p>&nbsp;</p>
      <ol start="3" type="1">
        <li>Concerning the requisites for wave lengths it is       sufficient to note that wave 3 in an impulsive 5-wave move is usually the       longest wave of the 5 waves, and <em>never</em> the shortest. </li>
      </ol>
      <ol start="4" type="1">
        <li>Of the three impulsive waves in a 5-wave sequence (the       other two being corrective) two of the three waves tend toward equality,       usually, but not always, waves one and five. </li>
      </ol>
      <p>&nbsp;</p>
      <ol start="5" type="1">
        <li>In like manner, waves two and four tend toward equality       (in terms of distance covered).</li>
      </ol>
      <ol start="6" type="1">
        <li>In a clear rising or falling trend, the bottom of wave       four must never penetrate the bottom of wave two; otherwise your wave       count is incorrect.</li>
      </ol>
      <p>&nbsp;</p>
      <ol start="7" type="1">
        <li>In many unfolding waves, the 4th wave reaction (also       known as &quot;corrective&quot;) is equal to the 1st wave impulse.       Similarly, the 5th wave impulse is often equivalent to the 2nd wave       reaction.</li>
      </ol>
      <ol start="8" type="1">
        <li>In corrective moves, waves A and C of the A-B-C       correction tend toward equality in most instances.</li>
      </ol>
      <p>&nbsp;</p>
      <ol start="9" type="1">
        <li>Wave B of the A-B-C correction must never penetrate       below the bottom of wave A.</li>
      </ol>
      <ol start="10" type="1">
        <li>A-B-C corrections, regardless of where they occur in a       wave sequence, typically retrace one-third of the previous 5-wave       sequence. A-B-C corrections tend to stop at the bottom of wave 4 in upward       trends and at the top of wave 4 in downward trends. </li>
      </ol>
      <p>&nbsp;</p>
      <ol start="11" type="1">
        <li>Fourth wave frequently take the form of a consolidation       and often take the form of triangles or pennants on the chart.</li>
      </ol>
