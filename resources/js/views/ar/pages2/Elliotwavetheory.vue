      <p>The Elliot Wave Theory is  multifractal in nature, which is defined as a geometric shape that can be  separated into parts, each of which is a reduced-scale version of the whole.  The multifractal nature of the Elliot Wave Principle is illustrated below:<br />
        In a nutshell, the Elliot Wave  cycles can be numbered as 5-3-5-3-5-5-3-5. Elliot Wave Theory postulates that,  regardless of size, a complete stock market cycle comprises eight movements.  Those eight movements comprise the first five impulsive waves and the first  three corrective waves of a market cycle.<br />
        At this point, the theory gets a  little more complex (and fascinating). Since Elliot Waves are multifractal,  each individual wave subdivides into a further series of waves identical in  form and number to the overall larger pattern of 5-3-5-3-5. For instance, wave  1 of a given impulsive Elliot Wave pattern subdivides into 5 additional waves.  Of these five subdivided waves, a further subdivision is found which  corresponds to the larger pattern in Pandora's Box fashion. Wave 2 subdivides  into three &quot;A-B-C&quot; waves, and so on.<br />
        These subdivided waves are  classified according to cycle degree. In formulating this theory, Elliot  labeled each of these cycles, from the very smallest to largest. The standard  Elliot Wave classification - from greatest to least - is as follows:<br />
