    <p><span >Chapter  2</span><br />
  The term &quot;Elliot Wave  Theory&quot; is being used more and more and more in the vocabulary of traders  and investors around the world today. The theory itself has been around for  many decades and popularized in the 1970s by Robert Prechter and A.J. Frost,  through their book, <em>Elliot Wave Principle</em>. It has been learned by  countless thousands of traders over the years and is increasingly incorporated  in computer trading programs.<br />
Despite all of this, however, there  seems to be a very low comprehension of its tenets among traders and almost no  consensus among practitioners of Elliot Wave Theory as to how the theory should  be applied in order to best maximize profits. The aim of this book is to  instruct the reader in the proper use of the theory for maximum profitability  in almost any market. We will show you how to avoid the most common pitfalls  that Elliot adherents encounter and how you can avoid the mistakes that most of  the leading exponents of the theory make.<br />
Before we do, however, we must lay  the foundation for understanding Elliot Wave. It is essential that the analyst  have a firm grasp of the basics of the theory and its application to financial  markets.<br />
In attempting to formally define the  Elliot Wave Theory, we quote from Frost &amp; Prechter in their seminal work, <em>Elliot  Wave Principle</em>.<br />
<em>The 'Elliot Wave Principle' is Ralph  Nelson Elliot's discovery that social, or crowd, behavior trends and reverses  in recognizable patterns. Using Stock market data for the Dow Jones Industrial  Average (DJIA) as his main research tool, Elliot discovered that the  ever-changing path of stock market prices reveals a structural design that in  turn reflects a basic harmony found in nature. From this discovery, he  developed a rational system of market analysis. Elliot isolated thirteen pattern,  or &quot;waves,&quot; that recur in markets and are repetitive in form, but are  not necessarily repetitive in time or amplitude. He named, defined and  illustrated the patterns. He then described how these structures link together  to form larger versions of the same patterns, how those in turn are the  building blocks for patterns of the next larger size, and so on. His  descriptions constitute a set of empirically derived rules and guidelines for  interpreting market action. Elliot claimed predictive value for the Wave  Principle, which now bears the name, &quot;The Elliot Wave Principle.&quot;</em><br />
Broadly speaking, the Elliot Wave  Theory (which we will henceforth abbreviate with EWT) can be summarized in the  following statement, also by Frost &amp; Prechter:<br />
<em>In markets, progress ultimately  takes form of five waves of a specific structure. Three of these waves, which  are labeled 1, 3 and 5, actually effect the directional movement. They are  separated by two counter trend interruptions, which are labeled 2 and 4... R.N.  Elliot did not specifically state that there is only one over-riding form, the  &quot;five wave&quot; pattern, but that is undeniably the case. At the time,  the market may be identified as being somewhere in the basic five wave pattern  at the largest degree trend. Because the five wave pattern is the overriding  form of market progress, all other patterns are subsumed by it. </em><br />
Before we can begin to discuss the  nuances of the theory, it will be helpful to define a couple of terms that  relate to EWT. There are two basic types of &quot;waves&quot; in any given  market-impulsive and corrective. Impulsive waves are those price movements that  are ascending in tandem with a rising trend or falling in a falling trend,  while corrective waves are those waves that are falling counter to a rising  trend, or rising in a falling trend. To provide for a better understanding of  this, the following illustration should help:<br />
A completed Elliot Wave Cycle  consists of five impulsive waves and three corrective waves:<br />
As you can see, the three wave  &quot;correction&quot; is labeled &quot;A-B-C,&quot; which is standard Elliot  Wave taxonomy. So then, the primary assumption of the Elliot Wave Theory is  that each movement, or wave, of a price trend consists of five components -  waves one through five. These five waves are followed by three corrective  waves.<br />
The term &quot;wave&quot; itself has  its origin from Dow Theory, as Charles Dow first made the analogy of stock  market movements as compared to the waves of the ocean. In his book, <em>Elliot  Wave Explained</em>, Robert Beckman observed that &quot;the waves are subordinate  to the tide, the ripples in the water subordinate to the waves, each rising and  falling with rhythmic regularity, self-generated and forming cross currents,  but the whole governed by seemingly moon-driven tidal forces.&quot; Beckman  further note that this comparison illustrates the various trends of share  price, &quot;sometimes acting concurrently, sometimes running contradictorily,  but always subject to an overriding force.&quot;<br />
Beckman summarizes the most  important rules governing Elliot Wave Theory as follows:<br />
For  every action there is a reaction. Stock market movements in the direction of  the main trend are impulsive moves. Stock market movements counter to the main  trend are corrective moves. An impulse move is always followed by a corrective  move.<br />
Generally,  all impulsive moves must have subordinate waves while all corrective moves have  three waves.</p>
