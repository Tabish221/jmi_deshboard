    <h2 >Candlestick Charts</h2>
    <p>Really the only choice for today's Forex traders, the candlestick chart  offers the same information as does a bar chart, but does so in a much more  ascetically pleasing manner. As is illustrated in the following image the  candlestick is comprised of a "body" and an upper and lower "wick". The body of  the candle is typically a dark color when the close is at a lower price than  was the open (a <em>bearish</em> candle). Conversely, if the close is at a higher  price than was the open the candle will be a light color (a <em>bullish</em> candle). The wick of the candle represents the entire range of price for that  period. The top of wick of course represents the price at its highest point,  while the bottom of the wick represents the price at its lowest point. <br />
